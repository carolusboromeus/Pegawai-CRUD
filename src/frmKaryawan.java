    //prosedur mengatur kolom jTable 
    private void AturKolom() 
    { 
	jTabel.setAutoResizeMode(javax.swing.JTable.AUTO_RESIZE_OFF);
	column = jTabel.getColumnModel().getColumn(0); 
	column.setPreferredWidth(80); 
	column = jTabel.getColumnModel().getColumn(1); 
	column.setPreferredWidth(170); 
	column = jTabel.getColumnModel().getColumn(2); 
	column.setPreferredWidth(50); 
	column = jTabel.getColumnModel().getColumn(3); 
	column.setPreferredWidth(170); 
	column = jTabel.getColumnModel().getColumn(4); 
	column.setPreferredWidth(100); 
    }
	
    //Prosedur menampilkan data ke jTable 
    private void IsiTabel() 
    { 
	DefaultTableModel tabelmapel = new DefaultTableModel(); 
	
	tabelmapel.addColumn("NIK"); 
	tabelmapel.addColumn("Nama"); 
	tabelmapel.addColumn("Kode"); 
	tabelmapel.addColumn("Departemen"); 
	tabelmapel.addColumn("Gaji"); 
	try 
	{ 
            objKaryawan.AksesData(); 
            objKaryawan.TampilData(); 
			
            Statement stat1 = objKaryawan.conn.createStatement(); 
            ResultSet res1=stat1.executeQuery(objKaryawan.sql); 
            while (res1.next()) 
            { 
		tabelmapel.addRow(new Object[]{res1.getString(1),res1.getString(2),res1.getString(3),res1.getString(4),res1.getString(5)}); 
            } 
            jTabel.setModel(tabelmapel); 
            AturKolom(); //atur kolom jTable 
	} 
	catch (Exception e) 
	{ 
	} 
    }
	
    //prosedur mengisi combobox dengan data 
    public void IsiComboDepartemen() 
    { 
	try 
            { 
		cboDepartemen.removeAllItems(); 
		objKaryawan.AksesData(); 
		String sql1 = "select * from tbldepartemen"; 
		Statement stat1 = objKaryawan.conn.createStatement(); 
		ResultSet res1=stat1.executeQuery(sql1); 
		cboDepartemen.addItem("Pilih Departemen"); 
		while(res1.next()) 
            { 
		cboDepartemen.addItem(res1.getString("KodeDep") + " : " + res1.getString("NamaDep")); 
            } 
	} 
	catch(Exception e) 
	{
	} 
    }
        
