    //prosedur simpan data 
    private void SimpanData() 
    { 
	if(txtNIK.getText().equals("")||txtNama.getText().equals("")) 
	{ 
            JOptionPane.showMessageDialog(null,"Data harus diisi semua"); 
	} 
	else 
	{ 
            try 
            { 
		objKaryawan.AksesData(); 
		objKaryawan.NIK = txtNIK.getText(); 
		objKaryawan.Nama = txtNama.getText(); 
		objKaryawan.KodeDep = cboDepartemen.getSelectedItem().toString().substring(0, 3); 
		String mGaji = txtGaji.getText(); 
		objKaryawan.Gaji = Integer.parseInt(mGaji); 
		objKaryawan.Simpan(); 
		
		objKaryawan.res.executeUpdate(objKaryawan.sql); 
		objKaryawan.conn.close(); 
		BersihIsian(); 
		IsiTabel(); 
		JOptionPane.showMessageDialog(null,"Data berhasil disimpan"); 
            } 
            catch (Exception e) 
            { 
		JOptionPane.showMessageDialog(null,"Proses penyimpanan gagal/koneksi gagal"); 
		System.out.println(e.getMessage()); 
            } 
	} 
    }
        
